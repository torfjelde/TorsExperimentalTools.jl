function generate_name(rng::Random.AbstractRNG; already_taken = Set{String}())
    name = rand(rng, LEFT_NAMES) * "_" * rand(rng, RIGHT_NAMES)
    if name in already_taken
        name *= string(uuid4())
    end
    return name
end
